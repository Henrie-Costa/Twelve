# Atividade Prática 02 - Calculadora de Média Escolar

# Notas fixas
nota1 = 7.5
nota2 = 8.0
nota3 = 6.5

# Cálculo da média
media = (nota1 + nota2 + nota3) / 3

# Saída formatada
print("Notas do aluno:")
print(f"Nota 1: {nota1:.2f}")
print(f"Nota 2: {nota2:.2f}")
print(f"Nota 3: {nota3:.2f}")
print(f"Média final: {media:.2f}")
